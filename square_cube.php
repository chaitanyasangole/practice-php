<?php 

function Square($num)
{
	return	$num * $num;
}


function Cube($num)
{
	return	$num * $num * $num;
}

function Add($num1 , $num2)
{
	return $num1 + $num2;
}

function Sub($num1 , $num2)
{
	return $num1 - $num2;
}

function Mul($num1,$num2)
{
	return $num1 * $num2;
}

function Div($num1,$num2)
{
	return $num1 / $num2;
}

?>